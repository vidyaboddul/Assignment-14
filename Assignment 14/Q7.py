# Q7. Arithmetic Operations

# Import NumPy library
import numpy as np

# Create two arrays
a = np.array([10, 20, 30, 40])
b = np.array([1, 2, 3, 4])

# Display the arrays
print("Array a:")
print(a)

print("\nArray b:")
print(b)

# Addition
print("\nAddition:")
print(a + b)

# Subtraction
print("\nSubtraction:")
print(a - b)

# Multiplication
print("\nMultiplication:")
print(a * b)

# Division
print("\nDivision:")
print(a / b)

# Element-wise Power
print("\nElement-wise Power (a ** b):")
print(a ** b)

# Modulo Operation
print("\nModulo Operation (a % b):")
print(a % b)